    // transforma Calendar em String
    private String dataFormatada(Calendar data){
        Calendar c = data;
        Date date = c.getTime();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        
        return sdf.format(date);
    }

    private void dadosCondomino() {
        labelNome.setText(this.condomino.getNome());
        labelCpf.setText(this.condomino.getCpf());
        labelDataNascimento.setText(dataFormatada(this.condomino.getDataNascimento()));
        labelEstadoCivil.setText(this.condomino.getEstadoCivil().getDescricao());
        
        for(String telefone : this.condomino.getTelefones())
            ((DefaultListModel)(listaTelefone.getModel()))
               .addElement(telefone);
        
        labelLogin.setText(this.condomino.getLogin());
        labelSenha.setText(this.condomino.getSenha());
        labelBloco.setText("" + this.condomino.getApartamento().getBloco());
        labelAndar.setText("" + this.condomino.getApartamento().getAndar());
        labelNumero.setText(this.condomino.getApartamento().getNumApartamento());
        
        if (this.condomino.getTipoUsuario() == TipoUsuario.ADMIN) {
            labelPerfil.setText("Administrador");
        } else {
            labelPerfil.setText("Condômino");
        }
    }

    

