    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        
        int resposta = JOptionPane.showConfirmDialog(this, "Deseja mesmo excluir o cond√¥mino: "+this.condomino.getNome()+"?", "Aviso", JOptionPane.YES_NO_OPTION);
        
        if(resposta == JOptionPane.YES_OPTION){
            CondominoDAO dao = new CondominoDAO();
            dao.excluiCondomino(this.condomino);
            JOptionPane.showMessageDialog(this, "Cond√¥mino exclu√≠do com sucesso!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        }
        
        
        
    }//GEN-LAST:event_botaoExcluirActionPerformed

